    private void rellenaDatos(Cliente cli) {
        if (cli != null) {

            jTextFieldApellidos.setText(cli.getApellidos());
            jTextFieldCodigo.setText("" + cli.getCodigo());
            jTextFieldDNI.setText(cli.getDni());
            jTextFieldDireccion.setText(cli.getDireccion());
            jTextFieldEdad.setText("" + cli.getEdad());
            jTextFieldEstadoCivil.setText(cli.getEstadoCivil());
            jTextFieldNombre.setText(cli.getNombre());
            jTextFieldTelefonoMovil.setText("" + cli.getTelefonoMovil());
            jTextFieldTelefonoFijo.setText("" + cli.getTelefonoFijo());

        }
    }

    private void inicio() {
        try {
            listaClientes = new ListaClientes();
            listaClientes.leeFichero();
            this.rellenaDatos(listaClientes.primerCliente());
        } catch (NullPointerException ex) {
            window.setMensaje("Fichero de lectura vacio", ex);
        }
    }

    private void guardarDatos(char operacion) {
        Cliente cli = new Cliente();
        // metodo para leer datos del formulario y setearlos en un cliente que le pasamos
        try {
            cli.setApellidos(jTextFieldApellidos.getText());
            cli.setCodigo(Integer.parseInt(jTextFieldCodigo.getText()));
            cli.setDni(jTextFieldDNI.getText());
            cli.setDireccion(jTextFieldDireccion.getText());
            cli.setEdad(Integer.parseInt(jTextFieldEdad.getText()));
            cli.setEstadoCivil(jTextFieldEstadoCivil.getText());
            cli.setNombre(jTextFieldNombre.getText());
            cli.setTelefonoFijo(Float.parseFloat(jTextFieldTelefonoFijo.getText()));
            cli.setTelefonoMovil(Float.parseFloat(jTextFieldTelefonoMovil.getText()));

            cli.setOperacion(operacion);
            // si la operacion es de insertar uso add y a√±ado nuevo velero
            if (cli.getOperacion() == 'I') {
                cli.setCodigo(listaClientes.getColeccionClientes().size() + 1);
                listaClientes.addCliente(cli);
                // si la operacion es actualizar recojo el indice y actualizo el velero
            } else if (operacion == 'U') {
                cli.setCodigo(listaClientes.getColeccionClientes().get(listaClientes.getIndice()).getCodigo());
                listaClientes.getColeccionClientes().set(listaClientes.getIndice(), cli);
            }
            //this.limpiarFormulario();
        } catch (NumberFormatException ex) {
            window.setMensaje("Formato incorrecto", ex);
        }
    }

